#include "../../includes/libft.h"

double			ft_sin(double x)
{
	int			i;
	double		r;
	double		s;

	i = 10;
	r = x * x;
	s = 42.0;
	while (i >= 10)
	{
		s = 4.0 * i - 2.0 + (-r) / s;
		i--;
	}
	return (2.0 * x * s / (r + s * s));
}
